// Na sequência, faça uma declaração do tipo `if else` para verificar qual número é maior
// que o outro. Depois, na sequência, escreva a mesma declaração com as condições
// invertidas ( ou seja, dentro da condição troque “maior que” para “menor que” ou 
// “menor que” para “maior que”)

let maior = 10;
let menor = 5;

if (maior > menor)
console.log(`${maior} é maior que ${menor}`);
else
console.log(`${menor} é maior que ${maior}`);

if (menor < maior)
console.log(`${menor} é menor que ${maior}`);
else
console.log(`${maior} é menor que ${menor}`);